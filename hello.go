	package main

import "fmt"

func main() {
	fmt.Println("Hello, " + "World!")

	fmt.Println("1+10 =", 1+10)
	fmt.Println("7/3.0 =", 7/3.0)
	fmt.Println("7/3 =", 7/3)
	fmt.Println("7%3 =", 7%3)
	fmt.Println("7^3 =", 7^3)
	fmt.Println("7*3 =", 7*3)
	fmt.Println("true AND false is", true && false)
	fmt.Println("true OR false is", true || false)
	fmt.Println("not true is", !true)
}
